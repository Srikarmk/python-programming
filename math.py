#few math modules
import math
a=float(input())
print(math.factorial(int(a)))  #returns factorial of a
print(round(a)) #returns round of a
print(math.ceil(a)) # returns round up
print(math.floor(a)) #returns round down
print(math.pow(3,2)) #return square of  number
print(math.sqrt(25)) #returns square root of number
print(math.gcd(25,30)) #returns gcd of 2 numbers 
